package com.emergentes;

public class Persona {
    private String Nombre;
    private int Edad;

    public Persona() {
        
    }

    public String getNombre() {
        return Nombre;
    }

    public int getEdad() {
        return Edad;
    }

    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }

    public void setEdad(int Edad) {
        this.Edad = Edad;
    }
    
    
}
